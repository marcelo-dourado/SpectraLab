readme = '''
Bem-vindo ao **SpectraLab**! Este aplicativo permite analisar dados de espectros UV-Vis e extrair informações sobre a absorbância para elementos específicos. Siga as instruções abaixo para usar o aplicativo:

### 1. **Requisitos para o uso ao app**

Para usar corretamente o aplicativo, os arquivos de espectro devem estar no formato CSV e seguir as especificações abaixo:

- O arquivo deve ter colunas separadas por `;`, com decimais representados por `,`.
- As colunas devem estar na ordem: comprimento de onda e intensidade (por exemplo, 'comprimento_de_onda;intensidade;').

### 2. **Upload dos espectros e seleção dos Elementos**

Faça o upload de todos os arquivos .csv que contém as informações espectrais. Em seguida, marque as checkboxes ao lado dos elementos que você deseja analisar. Cada checkbox representa um elemento específico com um comprimento de onda associado.

- **Exemplo**:
  - **Pr - 445 nm**: Selecionando esta opção, você estará analisando dados para o elemento Pr no comprimento de onda de 445 nm.
  - **Nd - 794 nm**: Selecionando esta opção, você estará analisando dados para o elemento Nd no comprimento de onda de 794 nm.

### 3. **Processar Dados**

Clique no botão **"Processar Dados"** para iniciar a análise. O aplicativo processará os arquivos de espectro e exibirá os resultados.

### 4. **Visualização dos Resultados**

Os resultados serão exibidos tanto na forma de um gráfico centralizado no pico de análise quanto na forma de uma tabela mostrando o comprimento de onda e a absorbância correspondente. No final da página, haverá um botão para download dos resultados.


Quaisquer dúvidas e sugestões de melhorias devem ser enviadas para mdourado@cetem.gov.br.
'''